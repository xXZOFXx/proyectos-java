    private void sueldoBasicKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sueldoBasicKeyPressed
        // TODO add your handling code here:
        
        char c = evt.getKeyChar();
        
        if(Character.isLetter(c) ){
        
            JOptionPane.showMessageDialog(null, " el campo sueldo solo acepta números");
            this.nomEmpleado.setText(null);

        this.sueldoBasic.setText(null);
        this.aServicio.setText(null);
        
        }else {
        
            sueldoBasic.setEditable(true);
        
        }
        
    }//GEN-LAST:event_sueldoBasicKeyPressed

