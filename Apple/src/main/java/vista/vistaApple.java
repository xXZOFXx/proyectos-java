    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
        // TODO add your handling code here:

        if (this.nomEmpleado.getText().equals("") || this.sueldoBasic.getText().equals("") || this.aServicio.getText().equals("") ) {
            
            

            JOptionPane.showMessageDialog(null, "por favor llena todos los campos correctamente");

            this.nomEmpleado.setText(null);

            this.sueldoBasic.setText(null);
            this.aServicio.setText(null);

            this.empleado.setText(null);
            this.bono.setText(null);
            this.SueldFinal.setText(null);
            

        } else {

            //entrada de nombre
            String entradaCaracter = this.nomEmpleado.getText();
            this.empleado.setText("" + entradaCaracter);

            //entrada de sueldo y proceso del 20%
            int sueldoUno = (int) Float.parseFloat(this.sueldoBasic.getText());

            float sueldoPorciento = (float) ((sueldoUno) * 0.20);

            float sueldoTotal = (sueldoPorciento + sueldoUno);

            this.SueldFinal.setText("Tu sueldo final es " + sueldoTotal + "pesos mexicanos");

            //finaliza la entrada  de sueldo y proceso del 20%
            //entrada de años trabajados
            // int  aTrabajados = Integer.parseInt(this.aServicio.getText());
            this.bono.setText("bono del 20%");

        }
            
          
      


    }//GEN-LAST:event_calcularActionPerformed

